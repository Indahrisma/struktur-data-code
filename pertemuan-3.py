def luaspersegi (sisi):
    luas = sisi*sisi
    return luas

print("Luas Persegi = %d" % luaspersegi(5))


def persegipanjang (panjang, lebar):
    luas = panjang*lebar
    return luas
print("Luas Persegi Panjang = %d" % persegipanjang(5, 7))


def volumekubus(sisi):
    volume = sisi*sisi*sisi
    return volume

print("volume kubus = %d" % volumekubus(5))